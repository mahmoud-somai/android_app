package com.example.projetanimal;

public class Animal {
    String name;
    String type;
    String age;
    String address;
    int phone;

    public Animal() {
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }

    public int getPhone() {
        return phone;
    }
}
